#include "libft.h"

void	ft_memdeltab(char **ap)
{
	size_t i;

	i = 0;
	if (ap)
	{
		while (ap[i])
		{
			free(ap[i]);
			ap[i++] = NULL;
		}
		free(ap);
	}
}
